// Homework: Strings

fun main() {
    // Challenge: save your name in a string variable and then print in uppercase and reversed.
    val name = "May Lye"
    println(name.uppercase())
    println("My uppercase name is ${name.uppercase()}.")
    println(name.reversed())
    println("Reverse of my name is ${name.reversed()}.")
    println(name.uppercase().reversed())
}